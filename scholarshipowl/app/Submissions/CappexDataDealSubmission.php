class CappexDataDealSubmission extends AbstractSubmission
{

    /**
     * @var SubmissionService
     */
    protected $ss;
    public function submissionSend($batch)
    {
        $submissionsCappexDataLead = $this->ss->getSubmissionsAccountsByNameAndStatus(Submission::NAME_CAPPEXDATADEAL,
            Submission::STATUS_INCOMPLETE, $batch);

        /** @var Submission $submission */
        foreach ($submissionsCappexDataLead as $submission) {
            $submissionId = $submission->getSubmissionId();

            try {
                $data = json_decode($submission->getParams(), true);


                if (empty($data['address']) || empty($data['zip_code']) ||
                    empty($data['city_name']) || empty($data['state_name'])) {
                    $account = $submission->getAccount();

                    if($account instanceof Account && $account->getProfile() != null) {
                        $profile = $account->getProfile();
                        $data['zip_code'] = $profile->getZip();
                        $data['address'] = $profile->getAddress();
                        $data['city_name'] = $profile->getCity();
                        $data['state_name'] = $profile->getState()->getAbbreviation();
                    }
                }


                $this->send($data);

                $response = $this->getResponse();

                if ($this->hasErrors()) {
                    \Log::error($this->errors);

                    $error = implode(',', $this->errors);
                    if($response != ''){
                        $error = $error.' '.$response;
                    }

                    $this->ss->updateErrorSubmittionSubmission($submissionId, $error);
                } else {
                    $this->ss->updateSuccessSubmission($submissionId, $response);
                }
            }
            catch (EntityNotFoundException $notFound){
                \Log::info("Account not found while sending submission [$submissionId]");
                $this->ss->updateErrorSubmission($submissionId, 'Account was deleted');
            }
            catch (\Throwable $exc) {
                \Log::error($exc);
                $this->ss->updateErrorSubmittionSubmission($submissionId, $exc->getMessage());
            }
        }
    }

    public function send($params = null)
    {
        $this->response = "";
        $this->rawResponse = "";
        $this->errors = array();

        if (isset($params['endpoint_url'])) {
            $endpoint = $params['endpoint_url'];
            unset($params['endpoint_url']);

            $url = $endpoint . "?" . http_build_query($params) . '&ResponseFormat=json';

            \Log::info("SUBMITTED TO CAPPEX DATA DEAL: " . $url);

            $this->curl->get($url);

            $this->response = $this->curl->response;
            $this->rawResponse = $this->curl->rawResponse;

            if ($this->curl->error) {
                throw new \RuntimeException($this->curl->rawResponse.' '.$this->curl->errorCode.' '.$this->curl->errorMessage);
            }
        } else {
            \Log::error("Can't submit cappex data deal submission. ".var_export($params, true));
        }

        return $this->onResponse();
    }

    public function onRequest($params = array())
    {
    }

    public function onResponse()
    {
        $response = json_decode(json_decode($this->rawResponse, true));

        if (isset($response->status) && $response->status != 'Accepted') {
            $this->errors[] = var_export($response, true);
        }

    }


    protected $em;

    public function __construct(SubmissionService $ss, EntityManager $em)
    {
        parent::__construct();

        $this->ss = $ss;
        $this->em = $em;
    }

    /**
     * Execute the console command.
     *
     * @return mixed
     */
    public function handle()
    {
        $this->info("Store GossamerScience started: " . date("Y-m-d h:i:s"));

        $coregPlugin = $this->getCoregPlugin();

        $doeCodes = implode(',', $this->getDoeCodes());

        $history = $this->em->getRepository(\App\Entity\Log\LoginHistory::class);

        $sqlTempCreate = " CREATE TEMPORARY TABLE universities_by_doe_code
            SELECT c.canonical_name, c.doe_code
            FROM college as c where c.doe_code in ($doeCodes);";

        $sqlTempDrop = "DROP TEMPORARY TABLE IF EXISTS universities_by_doe_code;";
        $sqlSelectData = "
            select 
            u.doe_code, 
            p.university, 
            a.account_id, 
            p.first_name, 
            p.last_name, 
            a.email,
            p.address, 
            p.address2, 
            p.city, 
            s.abbreviation as state, 
            if(p.school_level_id = 4,  p.highschool_graduation_year ,p.graduation_year) as graduation_year, 
            p.zip 
            from profile as p
                join universities_by_doe_code u on u.canonical_name = p.university
                left join account a on p.account_id = a.account_id
                left join state as s on p.state_id = s.state_id
                left JOIN submission sub on a.account_id = sub.account_id and sub.name = 'GossamerScience'
            where
                date(a.created_date) = CURRENT_DATE()
                and p.graduation_year in (2020, 2021, 2022, 2023, 2024)
                and p.school_level_id in (5,6,7,8)
                and sub.account_id is null
                and a.sell_information != 1;
              ";
        try {

            \DB::statement($sqlTempCreate);
            $result = \DB::select($sqlSelectData);
            foreach ($result as $record) {
                $doeCode = str_pad($record->doe_code, 8, '0', STR_PAD_LEFT);
                $row = [
                    "feed" => self::GS_FEED,
                    "first_name" => $record->first_name,
                    "last_name" => $record->last_name,
                    "email" => $record->email,
                    "address_1" => $record->address,
                    "address_2" => $record->address2,
                    "city" => $record->city,
                    "state" => $record->state,
                    "postal_code" => $record->zip,
                    "grad_year_so" => $record->graduation_year,
                    "college_ipeds" => $doeCode,
                    "college_name" => $record->university
                ];

                $accountId = $record->account_id;

                $loginHistory = $history->findBy(
                    ['account' => $accountId],
                    ['loginHistoryId' => 'DESC']
                );

                $ip = isset($loginHistory[0]) ? $loginHistory[0]->getIpAddress() : '';

                $submission = $this->ss->addSubmissions(
                    [
                        Submission::NAME_GOSSAMERSCIENCE => [
                            'checked' => 1,
                            'extra'   => $row,
                            'id' => $coregPlugin->getId()
                        ]
                    ], $accountId, $ip, SubmissionSources::SYSTEM
                );
            }

        } catch (\Exception $e) {
            \CoregLogger::error("Fail on GossamerScience coreg submission: ". $e->getMessage());
        } finally {
            \DB::statement($sqlTempDrop);
        }

        $this->info("Store GossamerScience coreg Ended: " . date("Y-m-d h:i:s"));
    }

    /**
     * @return CoregPlugin
     */
    protected function getCoregPlugin(): CoregPlugin
    {
        /**
         * @var CoregPlugin $coregPlugin
         */
        $coregPlugin = $this->em->getRepository(CoregPlugin::class)->createQueryBuilder('cp')
            ->where('cp.name = :name')
            ->setParameter(':name', Submission::NAME_GOSSAMERSCIENCE )
            ->setCacheable(true)
            ->getQuery()
            ->getResult();

        return $coregPlugin[0];
    }

